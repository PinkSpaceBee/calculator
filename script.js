function add (a,b) {
    let result = (a + b).toFixed(1);
    return result;
}
function substract (a,b) {
    let result = (a - b).toFixed(1);
    return result;
}
function multiply (a,b) {
    let result = (a * b).toFixed(1);
    return result;
}
function divide (a,b) {
    let result = (a / b).toFixed(1);
    return result;
}


